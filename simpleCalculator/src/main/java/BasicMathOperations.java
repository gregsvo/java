public class BasicMathOperations {
    
    

    public double addValues(Double x, Double y) {
        double solution = x + y;
        return solution;
    }
    
    public double subtractValues(Double x, Double y) {
        double solution = x - y;
        return solution;
    }
    
    public double multiplyValues(Double x, Double y) {
        double solution = x * y;
        return solution;
        
    }
    
    public double divideValues(Double x, Double y) {
        double solution = x / y;
        return solution;
    }
    
    
    
}